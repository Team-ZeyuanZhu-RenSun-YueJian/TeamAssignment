    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        Integer id = (Integer)jTable1.getValueAt(jTable1.getSelectedRow(), 0);
        ResultSet rs = orderDao.selectSelfOrder(id);
        Boolean falg = true;
        try{
            while(rs.next()){
                if(("0").equals(rs.getString("if_return"))){
                    falg = false;
                }
            }    
        }catch(Exception e){}
        if(falg == false){
            JOptionPane.showMessageDialog(null, "该用户有订单未还车，暂不能删除");
        }else{
            Boolean deleteFlag = userDao.deleteUser(id);
            if(deleteFlag == true) {
                    JOptionPane.showMessageDialog(null, "delete successfullt");
                    try {
                                    initTable();
                            } catch (SQLException e1) {
                                    e1.printStackTrace();
                            }
            }else {
                    JOptionPane.showMessageDialog(null, "delete failed");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    @SuppressWarnings({ "unchecked", "rawtypes" })
	public void initTable() throws SQLException {
		DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
		dtm.setRowCount(0);
		ResultSet rs = userDao.selectAllByType("租车人");
		while (rs.next()) {
			Vector v = new Vector();
			v.add(rs.getInt("id"));
			v.add(rs.getString("user_name"));
			v.add(rs.getString("user_number"));
			v.add(rs.getString("user_password"));
			dtm.addRow(v);
		}
	}
    
